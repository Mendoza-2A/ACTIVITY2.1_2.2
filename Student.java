class Student {
    private int Student_Id;
    private String Name;

    // Setter for Student_Id
    public void setStudent_Id(int id) {
        this.Student_Id = id;
    }

    // Getter for Student_Id
    public int getStudent_Id() {
        return Student_Id;
    }

    // Setter for Name
    public void setName(String name) {
        this.Name = name;
    }

    // Getter for Name
    public String getName() {
        return Name;
    }
}